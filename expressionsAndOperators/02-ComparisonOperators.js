/*
    COMPARISON OPERATORS
-Equal (==) is testing if variables are equal.
-Strict equal (===) this overrides JS coercion.
-Not Equal (!=)
-Strict not equal (!==)
-Greater than (>)
-Less than (<)
-Greater than or equal (>=) not to be confused with FAT ARROW (<=) function.
-Lesser than or equal (<=)
-And (&&)
-Or (||)
*JS is helpful and does something known as coercion when comparing values. Coercion is the process of converting a value from one type to another. In the below example, JS assumed that the were trying to check if '3' is equal to 3, even though one of our values is a string and one is an integer.
*/

'3' == 3;
console.log('3' == 3);